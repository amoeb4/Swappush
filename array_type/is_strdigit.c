#include"pushswap.h"

int	is_strdigit(char *str)
{
	int	i;
	int	k;

	k = 0;
	i = 0;
	if (str[0] == '-' || str[0] == '+')
		i++;
	while (str[i])
	{
		if (ft_isdigit(str[i]) == 0)
			return (1);
		i++;
		k = 1;
	}
	if (i <= 1 && k == 0)
		return (1);
	return (0);
}
